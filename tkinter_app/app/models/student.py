class Student:
    def __init__(self, id, first_name, last_name, dob, gender, grade, contact_number, address):
        self.id = id
        self.first_name = first_name
        self.last_name = last_name
        self.dob = dob
        self.gender = gender
        self.grade = grade
        self.contact_number = contact_number
        self.address = address
